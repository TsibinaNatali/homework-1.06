# Напишите функцию sort_even_indices(arr),
# которая берет элементы исходного списка, стоящие только на четных индексах (0, 2, 4 и т.д.),
# собирает их в новый список и сортирует этот новый список пузырьком по возрастанию.
# Функция должна вернуть отсортированный список.
# Подсказка для фильтрации:
# можно использовать срез с шагом [::2].
def sort_even_indices(arr):
    list = arr[::2]
    for i in range(len(list)):
        for j in range(0,len(list)-i-1):
            if list[j] > list[j+1]:
                list[j],list[j+1] = list[j+1],list[j]
    return list








