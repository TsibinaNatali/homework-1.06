# Задача 1.
# Работа со строками и условиями
# Напишите функцию clean_and_count(text), которая:
# Удаляет все пробелы в начале и конце строки.
# Считает количество символов в очищенной строке.
# Если длина строки больше 10 символов, функция должна вернуть эту строку в верхнем регистре (БОЛЬШИМИ БУКВАМИ).
# В противном случае — в нижнем регистре.
# Подсказка методов строк: .strip(), len(), .upper(), .lower().
def clean_and_count(text):
    text_delete = text.strip()
    if len(text_delete) > 10:
        return text_delete.upper()
    else:
        return text_delete.lower()

